#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*r;

	while (*s)
	{
		if (*s == (char)c)
			r = s;
		s++;
	}
	if (*r == (char)c)
		return (*r);
	return (NULL);
}
